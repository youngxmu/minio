CREATE DATABASE IF NOT EXISTS sucai_meta
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE sucai_meta;

CREATE TABLE IF NOT EXISTS meta_source (
  id BIGINT NOT NULL AUTO_INCREMENT,
  source_id VARCHAR(64) NOT NULL,
  source_name VARCHAR(128) NOT NULL,
  endpoint VARCHAR(512) NOT NULL,
  minio_version VARCHAR(128) NOT NULL,
  service_type VARCHAR(32) NOT NULL,
  status VARCHAR(32) NOT NULL DEFAULT 'ACTIVE',
  config_json JSON NULL,
  created_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  updated_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),
  PRIMARY KEY (id),
  UNIQUE KEY uk_meta_source_source_id (source_id),
  KEY idx_meta_source_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS meta_target (
  id BIGINT NOT NULL AUTO_INCREMENT,
  target_id VARCHAR(64) NOT NULL,
  target_name VARCHAR(128) NOT NULL,
  endpoint VARCHAR(512) NOT NULL,
  minio_version VARCHAR(128) NOT NULL,
  cold_bucket VARCHAR(255) NOT NULL,
  cold_prefix VARCHAR(1024) NOT NULL,
  tier_name VARCHAR(128) NOT NULL,
  usable_free_bytes BIGINT UNSIGNED NOT NULL DEFAULT 0,
  status VARCHAR(32) NOT NULL DEFAULT 'ACTIVE',
  config_json JSON NULL,
  created_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  updated_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),
  PRIMARY KEY (id),
  UNIQUE KEY uk_meta_target_target_id (target_id),
  UNIQUE KEY uk_meta_target_tier (target_id, cold_bucket, tier_name),
  KEY idx_meta_target_status (status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS meta_migration_batch (
  id BIGINT NOT NULL AUTO_INCREMENT,
  batch_id VARCHAR(128) NOT NULL,
  source_id VARCHAR(64) NOT NULL,
  target_id VARCHAR(64) NOT NULL,
  source_bucket VARCHAR(255) NOT NULL,
  cold_bucket VARCHAR(255) NOT NULL,
  cold_prefix VARCHAR(1024) NOT NULL,
  tier_name VARCHAR(128) NOT NULL,
  status VARCHAR(32) NOT NULL DEFAULT 'CREATED',
  max_migratable_bytes BIGINT UNSIGNED NULL,
  planned_object_bytes BIGINT UNSIGNED NOT NULL DEFAULT 0,
  transitioned_object_bytes BIGINT UNSIGNED NOT NULL DEFAULT 0,
  planned_object_count BIGINT UNSIGNED NOT NULL DEFAULT 0,
  transitioned_object_count BIGINT UNSIGNED NOT NULL DEFAULT 0,
  manifest_uri VARCHAR(1024) NULL,
  started_at DATETIME(6) NULL,
  completed_at DATETIME(6) NULL,
  created_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  updated_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),
  PRIMARY KEY (id),
  UNIQUE KEY uk_meta_migration_batch_batch_id (batch_id),
  KEY idx_meta_migration_batch_source_status (source_id, status),
  KEY idx_meta_migration_batch_target_status (target_id, status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS meta_video (
  id BIGINT NOT NULL AUTO_INCREMENT,
  source_id VARCHAR(64) NOT NULL,
  company_id BIGINT NOT NULL,
  station_id BIGINT NOT NULL,
  video_id BIGINT NOT NULL,
  user_id BIGINT NULL,
  account_id BIGINT NULL,
  video_status VARCHAR(64) NULL,
  business_create_time DATETIME NULL,
  business_update_time DATETIME NULL,
  migration_status VARCHAR(32) NOT NULL DEFAULT 'PENDING',
  required_object_count INT UNSIGNED NOT NULL DEFAULT 0,
  verified_object_count INT UNSIGNED NOT NULL DEFAULT 0,
  transitioned_object_count INT UNSIGNED NOT NULL DEFAULT 0,
  mapped_object_count INT UNSIGNED NOT NULL DEFAULT 0,
  created_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  updated_at DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),
  PRIMARY KEY (id),
  UNIQUE KEY uk_meta_video_identity (source_id, company_id, station_id, video_id),
  KEY idx_meta_video_company_station_status (company_id, station_id, migration_status),
  KEY idx_meta_video_video_id (video_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS meta_batch_video (
